package utilitaire;

public class Date {
    private int jour;
    private int mois;
    private int an;
    private int heure;
    private int minutes;
    private int seconde;

    public Date(int jour, int mois, int an, int heure, int minutes, int seconde){
        this.jour = jour;
        this.mois = mois;
        this.an = an;
        this.heure = heure;
        this.minutes = minutes;
        this.seconde = seconde;
    }
}
